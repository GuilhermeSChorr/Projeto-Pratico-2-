     private void montaTabela() {
        ArrayList<Jogos> jogos = controlaJogos.recuperarTodos();
        if (jogos == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar jogos");
        } else {
            tblJogos.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "ID";
                        case 1:
                            return "Nome";
                        case 2:
                            return "Quantidade";
                        case 3: 
                            return "Preço";
                        
                    }
                    return null;
                }

                @Override
                public int getColumnCount() {
                    return 4;
                }

                @Override
                public int getRowCount() {
                    return jogos.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Jogos j = jogos.get(rowIndex);

                    if (j != null) {
                        switch (columnIndex) {
                            case 0:
                                return j.getId();
                            case 1:
                                return j.getNome();
                            case 2:
                                return j.getQuantidadeEstoque();
                            case 3:
                                return j.getPreco();
                            
                        }

                    }

                    return "n/d";
                }
            });

            tblJogos.getColumnModel().getColumn(0).setMinWidth(80);
            tblJogos.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblJogos.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }


