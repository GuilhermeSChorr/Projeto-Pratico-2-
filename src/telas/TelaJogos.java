    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
     String idString = String.valueOf(tblJogos.getValueAt(tblJogos.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        Jogos j = controlaJogos.recuperar(id);

        if (j == null) {
            JOptionPane.showMessageDialog(null, "Registro não localizado!");
        } else {

            // preenche a variavel codigo que sera usada para salvar ou editar
            codigo = j.getId();

            // mudar a aba ativa para Cadastro
            containertabela.setSelectedIndex(0);

            // coloca dados nos campos de interface
            txtNomeJogos.setText(j.getNome());
            txtquantidade.setText(String.valueOf(j.getQuantidadeEstoque()));
            txtpreco.setText(String.valueOf(j.getPreco()));
            
            txtNomeJogos.requestFocus();
        }
       
       
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       
            String idString = String.valueOf(tblJogos.getValueAt(tblJogos.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = controlaJogos.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }
    
                                              

        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnsalvarjogosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarjogosActionPerformed
        Jogos j = new Jogos();
        String nome = txtNomeJogos.getText();
        j.setNome(nome);

        String idString = txtidjogos.getText();
        int id = Integer.parseInt(idString);

        String precoString = txtpreco.getText();
        double preco = Double.parseDouble(precoString);
        j.setPreco(preco);

        String intestoque = txtquantidade.getText();
        int QuantidadeEstoque = Integer.parseInt(intestoque);
        j.setQuantidadeEstoque(QuantidadeEstoque);

        j.setId(id);

    

        if (codigo == 0) {
            boolean retorno = controlaJogos.salvar(j);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                txtNomeJogos.setText("");
                txtquantidade.setText("");
                txtpreco.setText("");

                txtNomeJogos.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        } else {
            j.setId(codigo);
            boolean retorno = controlaJogos.editar(j);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Editado com sucesso");
                txtNomeJogos.setText("");
                txtquantidade.setText("");
                txtpreco.setText("");

                montaTabela();
                containertabela.setSelectedIndex(1);
                codigo = 0;
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        }
    }//GEN-LAST:event_btnsalvarjogosActionPerformed


