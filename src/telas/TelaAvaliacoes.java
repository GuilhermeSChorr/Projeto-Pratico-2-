    private void montaTabela() {
        ArrayList<Avaliacoes> Aval = controlaAvaliacoes.recuperarTodos();
        if (Aval == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar jogos");
        } else {
            tblAvaliacoes.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "ID";
                        case 1:
                            return "Nome";
                        case 2:
                            return "Jogo";
                        case 3: 
                            return "Nota";
                        case 4:
                            return "Comentário";
                    }
                    return null;
                }

                @Override
                public int getColumnCount() {
                    return 5;
                }

                @Override
                public int getRowCount() {
                    return Aval.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Avaliacoes a = Aval.get(rowIndex);

                    if (a != null) {
                        switch (columnIndex) {
                            case 0:
                                return a.getId();
                            case 1:
                                return a.getNome();
                            case 2:
                                return a.getJogo();
                            case 3:
                                return a.getNota();
                            case 4:
                                return a.getComentario();
                            
                        }

                    }

                    return "n/d";
                }
            });

            tblAvaliacoes.getColumnModel().getColumn(0).setMinWidth(80);
            tblAvaliacoes.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblAvaliacoes.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

