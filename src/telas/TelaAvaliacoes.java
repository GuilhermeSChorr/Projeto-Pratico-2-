    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        String idString = String.valueOf(tblAvaliacoes.getValueAt(tblAvaliacoes.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = controlaAvaliacoes.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
  String idString = String.valueOf(tblAvaliacoes.getValueAt(tblAvaliacoes.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        Avaliacoes Aval = controlaAvaliacoes.recuperar(id);

        if (Aval == null) {
            JOptionPane.showMessageDialog(null, "Registro não localizado!");
        } else {

            // preenche a variavel codigo que sera usada para salvar ou editar
            codigo = Aval.getId();

            // mudar a aba ativa para Cadastro
            containertabela.setSelectedIndex(0);

            // coloca dados nos campos de interface
            txtNomeAval.setText(Aval.getNome());
            txtJogoAval.setText(Aval.getJogo());
           txtNotaAval.setText(String.valueOf(Aval.getNota()));
            txtComentarioAval.setText(Aval.getComentario());

            txtNomeAval.requestFocus();
        }

    }//GEN-LAST:event_btnEditarActionPerformed


