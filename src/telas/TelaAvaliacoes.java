    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Avaliacoes Aval = new Avaliacoes();
        String nome = txtNomeAval.getText();
        Aval.setNome(nome);
        Aval.setJogo(txtJogoAval.getText());
      
        String intnota = txtNotaAval.getText();
        int NotaAval = Integer.parseInt(intnota);
        Aval.setNota(NotaAval);
        
        Aval.setComentario(txtComentarioAval.getText());
        String idString = txtIdAval.getText();
        int id = Integer.parseInt(idString);
        Aval.setId(id);

        System.out.println(Aval);

         if (codigo == 0) {
            boolean retorno = controlaAvaliacoes.salvar(Aval);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                txtNomeAval.setText("");
                txtJogoAval.setText("");
                txtNotaAval.setText("");
                txtComentarioAval.setText("");

                txtNomeAval.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        } else {
            Aval.setId(codigo);
            boolean retorno = controlaAvaliacoes.editar(Aval);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Editado com sucesso");
                txtNomeAval.setText("");
                txtJogoAval.setText("");
                txtNotaAval.setText("");
                txtComentarioAval.setText("");

                montaTabela();
                containertabela.setSelectedIndex(1);
                codigo = 0;
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

